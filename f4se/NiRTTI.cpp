NiRTTI * RelocateNiRTTI(NiRTTI * rva)
{
	NiRTTI * result = nullptr;

	if(rva)
	{
		uintptr_t rvaInt = uintptr_t(rva);
		result = (NiRTTI *)(rvaInt + RelocationManager::s_baseAddr);
	}

	return result;
}

NiObject * DoNiRTTICast(NiObject * src, const NiRTTI * typeInfo)
{
	if(src)
		for(NiRTTI * iter = src->GetRTTI(); iter; iter = iter->parent)
			if(iter == typeInfo)
				return src;

	return nullptr;
}

